"""
Differences from src/search_differential_trail.py:
  - The (start_step, end_step, message_bound, message_differential, op0..op9)
    configuration is generated by config_gen for the target round, not hardcoded.
  - The two 37-step-specific hardcoded Hamming-weight bounds in assign_value
    (W22+W23 = 4 and the six-word = 35) are REMOVED.
  - obj_value ranges are clamped so small end_step values do not crash, with a
    Hamming-weight fallback when the uncontrolled Boolean window is empty.
  - Each stp call is wrapped with a per-call timeout; per-iteration solve times
    are logged, and the final condition count / HE / HW are recorded.
  - Given a round + LC json, it tries local-collision candidates (largest span
    first, since span <= 8 leaves no room for the state collision) until one
    yields a satisfiable DC.

op9 (value transitions) is kept off, faithful to the validated 37-step config.
(Note: src/unit_function_256.py:sha2_value has a variable-naming bug for the A
registers, so enabling op9 would need that fixed first.)
"""

import sys
import os
import subprocess
import time
import json

HERE = os.path.dirname(os.path.abspath(__file__))
sys.path.insert(0, os.path.join(HERE, "src"))

from unit_function_256 import (sha_e, sha_a, message_expand, sha2_value,  # noqa: E402
                               message_expand_value, read_differential_characteristic)
from config_gen import gen_config, with_value_transitions  # noqa: E402

RESULTS_DC = os.path.join(HERE, "results_dc")
WORK = os.path.join(RESULTS_DC, "_work")
BLOCK = 32
# Use all cores by default; override with SHA2_THREADS=N. (No artificial cap.)
THREADS = os.environ.get("SHA2_THREADS", str(os.cpu_count() or 4))


class DCModel:
    def __init__(self, cfg):
        self.cfg = cfg
        self.start = cfg["start_step"]
        self.end = cfg["end_step"]
        self.mb = cfg["message_bound"]
        self.msgdiff = cfg["message_differential"]
        self.op = [cfg["op%d" % k] for k in range(10)]
        self.declare = []
        self.constraints = []

    def check_assign(self, s):
        if s not in self.declare:
            self.declare.append(s)

    def main(self):
        for i in range(self.start, self.end):
            var_e, con_e = sha_e(BLOCK, self.op[0][i], self.op[1][i], self.op[2][i], i)
            self.constraints.append("".join(con_e))
            for v in var_e:
                self.check_assign(v)
            var_a, con_a = sha_a(BLOCK, self.op[3][i], self.op[4][i], self.op[5][i], i)
            self.constraints.append("".join(con_a))
            for v in var_a:
                self.check_assign(v)
            # op9: value-transition model (sha2_value + derive_cond, real K_i),
            # plus message_expand_value for expanded words. This is the O5-stage
            # validity model in the 2026 procedure; mirrors src/search_differential_trail.py.
            if self.op[9][i]:
                var_v, con_v = sha2_value(BLOCK, "IF", "MAJ", i)
                self.constraints.append("".join(con_v))
                for v in var_v:
                    self.check_assign(v)
                if i > 15:
                    var_mv, con_mv = message_expand_value(BLOCK, i)
                    self.constraints.append("".join(con_mv))
                    for v in var_mv:
                        self.check_assign(v)
        for i in range(self.mb):
            if i > 15:
                var_w, con_w = message_expand(BLOCK, self.op[6][i], self.op[7][i], self.op[8][i], i)
                self.constraints.append("".join(con_w))
                for v in var_w:
                    self.check_assign(v)

    def build(self):
        """Populate self.declare / self.constraints; return (variable_str, constrain_str)."""
        self.declare = []
        self.constraints = []
        self.main()
        self.assign_value()
        return "".join(self.declare), "".join(self.constraints)

    def declared_set(self):
        return set(line.split(":")[0].strip() for line in self.declare)

    def _declared_message_words(self):
        # Only message words actually declared by sha_e/message_expand exist as STP
        # symbols; zeroing an undeclared word triggers "Unresolved symbol".
        declared = set()
        for line in self.declare:
            if line.startswith("wv_"):
                declared.add(int(line.split("_")[1]))
        return declared

    def assign_value(self):
        declared_w = self._declared_message_words()
        # zero the difference of every DECLARED message word NOT in the local collision
        for i in range(self.mb):
            if i in declared_w and i not in self.msgdiff:
                for j in range(BLOCK):
                    self.constraints.append(
                        "ASSERT wv_%d_%d = 0bin0;\nASSERT wd_%d_%d = 0bin0;\n" % (i, j, i, j))
        # require at least one active difference bit among the local-collision words
        terms = []
        for idx in self.msgdiff:
            for j in range(BLOCK):
                terms.append("0bin000000000@wd_%d_%d" % (idx, j))
        self.constraints.append("ASSERT BVGE(BVPLUS(10," + ",".join(terms) + "), 0bin0000000001);\n")
        # zero the state difference before the local collision
        for step in range(self.start - 4, self.start):
            for i in range(BLOCK):
                self.constraints.append(
                    "ASSERT xv_%d_%d = 0bin0;\nASSERT xd_%d_%d = 0bin0;\n"
                    "ASSERT yv_%d_%d = 0bin0;\nASSERT yd_%d_%d = 0bin0;\n" % (
                        step, i, step, i, step, i, step, i))
        # zero A in the collision tail (last 8 state steps) and E (last 4)
        for step in range(self.end - 8, self.end):
            for i in range(BLOCK):
                self.constraints.append("ASSERT xv_%d_%d = 0bin0;\nASSERT xd_%d_%d = 0bin0;\n" % (step, i, step, i))
        for step in range(self.end - 4, self.end):
            for i in range(BLOCK):
                self.constraints.append("ASSERT yv_%d_%d = 0bin0;\nASSERT yd_%d_%d = 0bin0;\n" % (step, i, step, i))
        # NOTE: the 37-step-specific Hamming-weight bounds are intentionally omitted.

    # ---- staged objective TERM builders (each returns a list of 1-bit var names) ----
    # Terms are filtered against the declared set so the objective never references
    # an undeclared symbol (which STP rejects). This also makes O4/O5 sum over
    # exactly the modelled trail.
    def _O1_terms(self, declared):
        """O1: Hamming weight of the highest-index active message word(s)
        (the paper's H(dW22)+H(dW23)). Two highest if >=2 active words, else one."""
        active = sorted(self.msgdiff)
        last = active[-2:] if len(active) >= 2 else active
        return [t for i in last for t in self._wd(i, declared)]

    def _O2_terms(self, declared):
        """O2: total Hamming weight of all message-word differences."""
        return [t for i in sorted(self.msgdiff) for t in self._wd(i, declared)]

    def _O3_terms(self, declared):
        """O3: uncontrolled differential conditions on E16.. = Boolean bit
        conditions (Sigma1 XOR + IF, with variable-specific (t1,t2) counters) over
        [17,end) + Hamming weight H(dE) over [16,end-4). This is the attack's
        differential-condition count (= the differential-probability exponent)."""
        terms = []
        for i in range(17, self.end):
            if self.op[0][i] == 1:
                terms += self._named("ned_xor", i, declared)
            if self.op[1][i] in (1, 4):
                terms += self._named("nev_if", i, declared)
                terms += self._named("ned_if", i, declared)
            elif self.op[1][i] in (2, 3, 5):
                terms += self._named("ned_if", i, declared)
        for i in range(16, self.end - 4):
            terms += self._yd(i, declared)
        return terms

    def _O4_terms(self, declared):
        """O4: total Hamming weight of the A-register differences over the trail."""
        return [t for i in range(self.mb) for t in self._xd(i, declared)]

    def _O5_terms(self, declared):
        """O5: total Hamming weight of the E-register differences over the trail."""
        return [t for i in range(self.mb) for t in self._yd(i, declared)]

    @staticmethod
    def _term(var):
        return "0bin000000000@%s" % var  # pad a 1-bit var to 10 bits for BVPLUS(10)

    def _wd(self, i, declared):
        return [self._term("wd_%d_%d" % (i, j)) for j in range(BLOCK)
                if "wd_%d_%d" % (i, j) in declared]

    def _yd(self, i, declared):
        return [self._term("yd_%d_%d" % (i, j)) for j in range(BLOCK)
                if "yd_%d_%d" % (i, j) in declared]

    def _xd(self, i, declared):
        return [self._term("xd_%d_%d" % (i, j)) for j in range(BLOCK)
                if "xd_%d_%d" % (i, j) in declared]

    def _named(self, prefix, i, declared):
        return [self._term("%s_%d_%d" % (prefix, i, j)) for j in range(BLOCK)
                if "%s_%d_%d" % (prefix, i, j) in declared]


def count_hamming(out_file, message_bound):
    """Count active difference bits (v,d != 0,0) for E (y), W (w), A (x)."""
    data = open(out_file).read().split("\n")
    vv = {"x": {}, "y": {}, "w": {}}
    dd = {"x": {}, "y": {}, "w": {}}
    for line in data:
        if " = " not in line:
            continue
        name = line.replace("ASSERT( ", "").split(" = ")[0].strip()
        val = line.split(" = ")[1].replace(" );", "").strip()
        bit = 1 if "1" in val else 0
        for pref in ("xv", "xd", "yv", "yd", "wv", "wd"):
            if name.startswith(pref + "_"):
                reg = pref[0]
                kind = "v" if pref[1] == "v" else "d"
                key = name[len(pref) + 1:]
                (vv if kind == "v" else dd)[reg][key] = bit
    counts = {}
    for reg in ("x", "y", "w"):
        active = 0
        keys = set(vv[reg]) | set(dd[reg])
        for k in keys:
            if vv[reg].get(k, 0) or dd[reg].get(k, 0):
                active += 1
        counts[reg] = active
    return counts["y"], counts["w"], counts["x"]


def build_specs(R, lc_json=None):
    """Assemble local-collision specs to try, best-first, SOLELY from the
    local-collision search output (results_lc/lc_R.json). No presets: every spec
    is a from-scratch LC-search result. The 'best' LC is tried first; the
    'alternates' (also LC-search outputs) are fallbacks if it is infeasible."""
    if lc_json is None:
        lc_json = os.path.join(HERE, "results_lc", "lc_%d.json" % R)
    if not os.path.exists(lc_json):
        raise FileNotFoundError(
            "No local collision for R=%d at %s. Run lc_search.py %d first "
            "(the LC search is the sole, preset-free source of local collisions)."
            % (R, lc_json, R))
    data = json.load(open(lc_json))
    specs = [dict(data["best"], source="lc-search-best")]
    for alt in data.get("alternates", []):
        specs.append(dict(alt, source="lc-search-alt"))
    # de-duplicate on (start, span, active words)
    seen = set()
    uniq = []
    for s in specs:
        key = (s["start_step"], s["span"], tuple(s["active_words"]))
        if key not in seen:
            seen.add(key)
            uniq.append(s)
    # Feasibility ordering (NOT a preset -- all specs are LC-search outputs):
    # the tail-zeroing in assign_value (zero A over [end-8,end)) needs span >= ~9
    # to leave room for the collision's A/E differences, so a span-6 collision is
    # structurally infeasible. Try adequate-span candidates first, then by the
    # Algorithm-1 objective (fewest active words, then fewest cancellation
    # conditions). The LC-search 'best' still leads when it is feasibility-shaped.
    MIN_FEASIBLE_SPAN = 9
    uniq.sort(key=lambda s: (0 if s["span"] >= MIN_FEASIBLE_SPAN else 1,
                             s.get("num_active", len(s["active_words"])),
                             s.get("cond_total", 0),
                             s["span"]))
    return uniq



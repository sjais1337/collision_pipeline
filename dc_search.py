def _parse_obj(decoded, name):
    for line in decoded.split("\n"):
        if name in line and " = " in line:
            raw = line.split(" = ")[1].replace(" );", "").replace(");", "").strip()
            if raw.startswith("0x"):
                return int(raw[2:], 16)
            if raw.startswith("0b"):
                return int(raw[2:], 2)
            try:
                return int(raw)
            except ValueError:
                return None
    return None


def _obj_def(name, terms, width=10):
    if not terms:
        return None
    return "%s: BITVECTOR(%d);\nASSERT %s = BVPLUS(%d,%s);\n" % (
        name, width, name, width, ",".join(terms))


def _run_stp(cvc, timeout):
    try:
        R = subprocess.check_output(["stp", cvc, "--cryptominisat", "--threads", THREADS],
                                    stderr=subprocess.STDOUT, timeout=timeout)
        return R.decode(), None
    except subprocess.TimeoutExpired:
        return None, "timeout"
    except subprocess.CalledProcessError as e:
        return None, "error:" + e.output.decode()[:150]


def descend_stage(name, terms, variable, base_constr, carried, tag, timeout,
                  query, width=10, budget=None):
    """Minimize objective `name` = sum(terms), subject to base + carried
    constraints, by descending the BVLE bound. Returns
    (min_value, out_file, carry_clause, iters, status)."""
    os.makedirs(WORK, exist_ok=True)
    iters = []
    obj_def = _obj_def(name, terms, width)
    cvc = os.path.join(WORK, "dc_%s_%s.cvc" % (tag, name))

    if obj_def is None:
        # trivial objective (no terms): just confirm feasibility / get a witness
        with open(cvc, "w") as f:
            f.write(variable); f.write(base_constr); f.write(carried); f.write(query)
        t0 = time.time(); decoded, err = _run_stp(cvc, timeout); dt = time.time() - t0
        if err:
            return None, None, "", [{"stage": name, "dt": round(dt, 2), "result": err.split(":")[0].upper()}], err.split(":")[0]
        if decoded.strip() == "Valid.":
            return None, None, "", [{"stage": name, "dt": round(dt, 2), "result": "UNSAT"}], "infeasible"
        out = os.path.join(WORK, "dc_%s_%s.out" % (tag, name))
        open(out, "w").write(decoded)
        return 0, out, "", [{"stage": name, "achieved": 0, "dt": round(dt, 2), "result": "SAT"}], "ok"

    bound = min((1 << width) - 1, len(terms))
    best_v, best_out, status = None, None, "ok"
    t_start = time.time()
    while True:
        if budget is not None and best_out is not None and (time.time() - t_start) > budget:
            iters.append({"stage": name, "result": "BUDGET"}); break
        with open(cvc, "w") as f:
            f.write(variable); f.write(base_constr); f.write(carried); f.write(obj_def)
            f.write("ASSERT BVLE(%s, 0bin%s);\n" % (name, bin(bound)[2:].zfill(width)))
            f.write(query)
        t0 = time.time(); decoded, err = _run_stp(cvc, timeout); dt = time.time() - t0
        if err:
            iters.append({"stage": name, "bound": bound, "dt": round(dt, 2), "result": err.split(":")[0].upper()})
            status = "ok" if best_out is not None else err.split(":")[0]
            break
        if decoded.strip() != "Valid.":  # SAT
            out = os.path.join(WORK, "dc_%s_%s_%d.out" % (tag, name, bound))
            open(out, "w").write(decoded)
            ach = _parse_obj(decoded, name)
            if ach is None:
                ach = bound
            iters.append({"stage": name, "bound": bound, "achieved": ach, "dt": round(dt, 2), "result": "SAT"})
            best_v, best_out = ach, out
            if ach == 0:
                break
            bound = ach - 1
        else:  # UNSAT -> optimum is the last SAT value
            iters.append({"stage": name, "bound": bound, "dt": round(dt, 2), "result": "UNSAT"})
            status = "ok" if best_out is not None else "infeasible"
            break

    carry = ""
    if best_v is not None:
        carry = obj_def + "ASSERT %s = 0bin%s;\n" % (name, bin(best_v)[2:].zfill(width))
    return best_v, best_out, carry, iters, status


def _cascade_result(status, final_out, optima, iters, mb):
    res = {
        "status": status,
        "found": final_out is not None,
        "min_conditions": optima.get("o3"),
        "stage_optima": optima,
        "iters": iters,
        "total_time": round(sum(it.get("dt", 0) for it in iters), 2),
        "out_file": final_out,
        "sat_outs": [final_out] if final_out else [],
    }
    if final_out is not None:
        he, hw, ha = count_hamming(final_out, mb)
        res.update({"HE": he, "HW": hw, "HA": ha})
    return res


def solve_cascade(cfg, tag, timeout=600, o5_value=True, budget=None, stop_after="o5"):
    """The 2026 staged objective, run as a real lexicographic cascade:
        O1 = H of highest-index active W(s)
        O2 = total H(dW)
        O3 = uncontrolled differential conditions on E16.. (Boolean BD + H(dE))
        O4 = total H(dA)
        O5 = total H(dE)
    Each stage is minimized, then fixed (ASSERT obj_k = v_k) before the next.
    O1-O4 run on the difference model; O5 optionally on the value-transition
    model (op9) for validity (the paper runs value transitions at this stage).
    O3's minimum is the reported differential-condition count.

    stop_after: stop the cascade after this stage ('o1'..'o5'). Use stop_after='o3'
    for a cheap O3 scan (skips the slow, cosmetic O4/O5) when selecting the best
    local collision by differential-condition count."""
    query = "\nQUERY FALSE;\nCOUNTEREXAMPLE;"
    diff = DCModel(cfg)
    var_d, con_d = diff.build()
    decl_d = diff.declared_set()

    carried, optima, all_iters, final_out, status = "", {}, [], None, "ok"
    for name, terms in [("o1", diff._O1_terms(decl_d)), ("o2", diff._O2_terms(decl_d)),
                        ("o3", diff._O3_terms(decl_d)), ("o4", diff._O4_terms(decl_d))]:
        v, out, carry, iters, st = descend_stage(name, terms, var_d, con_d, carried,
                                                  tag, timeout, query, budget=budget)
        all_iters += iters
        optima[name] = v
        if out is not None:
            final_out = out
        if st != "ok":
            status = st
            if out is None:  # stage infeasible/timeout with no witness -> stop
                return _cascade_result(status, final_out, optima, all_iters, cfg["message_bound"])
        carried += carry
        if name == stop_after:
            return _cascade_result(status if final_out else "no_solution",
                                   final_out, optima, all_iters, cfg["message_bound"])

    # O5
    if o5_value:
        val = DCModel(with_value_transitions(cfg))
        var5, con5 = val.build()
        o5_terms = val._O5_terms(val.declared_set())
        base_var, base_con = var5, con5
    else:
        o5_terms, base_var, base_con = diff._O5_terms(decl_d), var_d, con_d
    v, out, carry, iters, st = descend_stage("o5", o5_terms, base_var, base_con, carried,
                                             tag, timeout, query, budget=budget)
    all_iters += iters
    if out is not None:
        optima["o5"], final_out = v, out
    elif o5_value:
        # O5 value-model UNSAT/timeout: the DC may be invalid or just too hard
        # for the in-search validity model. Fall back to the difference-model O5
        # so we still emit the characteristic; find_collision is the final arbiter.
        status = "o5_value_" + st
        v2, out2, _, iters2, _ = descend_stage("o5", diff._O5_terms(decl_d), var_d, con_d,
                                               carried, tag + "_diffO5", timeout, query, budget=budget)
        all_iters += iters2
        if out2 is not None:
            optima["o5"], final_out = v2, out2
    if st != "ok" and status == "ok":
        status = "o5_" + st
    if final_out is not None and status == "ok":
        status = "minimized"
    return _cascade_result(status if final_out else "no_solution", final_out, optima, all_iters, cfg["message_bound"])


def _sat_out_files(res, tag):
    """Final-DC .out file(s) for validation, most-minimized first."""
    return [p for p in res.get("sat_outs", []) if p and os.path.exists(p)]


def run_specs(R, specs, timeout=300, max_attempts=8, init_pro=97,
              validate=True, oracle_timeout=300, max_validate=1, solve_budget=None,
              o5_value=True):
    """Search specs best-first, running the O1->O5 cascade per candidate. With
    validate=True a DC is accepted only when the SFS finder (find_collision.solve_dc)
    produces a verified colliding pair for it; on invalid_dc (UNSAT) we move to the
    next candidate. If the pair search only times out (the trail may still be valid
    but too hard for the solver to confirm in budget), we keep the DC as a
    best-effort artifact and record the timeout honestly."""
    attempts = []
    chosen = None
    chosen_collision = None
    chosen_out = None
    best_effort = None  # (res, out, coll) for the first found DC, if none verify
    if validate:
        import find_collision  # lazy: pulls in verify_collision / parse_dc
    for ci, c in enumerate(specs[:max_attempts]):
        cfg = gen_config(R, c["start_step"], c["start_step"] + c["span"], c["active_words"])
        tag = "R%d_c%d" % (R, ci)
        print("  [R=%d] try %d (%s): start=%d span=%d active=%s" % (
            R, ci, c.get("source", "?"), c["start_step"], c["span"], c["active_words"]))
        res = solve_cascade(cfg, tag, timeout=timeout, o5_value=o5_value, budget=solve_budget)
        res["candidate"] = c
        attempts.append(res)
        opt = res.get("stage_optima", {})
        print("    -> status=%s found=%s O1..O5=%s O3(cond)=%s total=%.1fs" % (
            res["status"], res["found"],
            [opt.get(k) for k in ("o1", "o2", "o3", "o4", "o5")],
            res["min_conditions"], res["total_time"]))
        if not res["found"]:
            continue
        if not validate:
            chosen, chosen_out = res, res["out_file"]
            break
        # oracle: validate this candidate's DC(s) by finding a real SFS pair
        verified = None
        last_coll = None
        for dc_out in _sat_out_files(res, tag)[:max_validate]:
            coll = find_collision.solve_dc(R, dc_out, timeout=oracle_timeout)
            last_coll = coll
            print("       validate %s -> %s" % (os.path.basename(dc_out), coll.get("status")))
            if coll.get("verified"):
                verified = (dc_out, coll)
                break
            if coll.get("status") in ("timeout", "error"):
                break  # oracle gave up; don't burn more time on this candidate
        if best_effort is None and res["out_file"]:
            best_effort = (res, res["out_file"], last_coll)
        if verified:
            chosen_out, chosen_collision = verified
            chosen = res
            print("    => DC validated: verified SFS colliding pair found")
            break
        if last_coll and last_coll.get("status") == "invalid_dc":
            print("    => DC has no conforming pair (invalid_dc); trying next candidate")
        else:
            print("    => pair search did not complete in budget; trying next candidate")

    # fall back to a best-effort DC (found but pair unverified) so we always emit
    # a characteristic artifact, with the honest collision status attached
    if chosen is None and best_effort is not None:
        chosen, chosen_out, chosen_collision = best_effort

    # point the chosen record at the actually-validated trail so parse_dc and
    # standalone find_collision reference the same DC the pair was built from
    if chosen and chosen_out:
        chosen["out_file"] = chosen_out
    summary = {"R": R, "chosen": chosen, "chosen_out": chosen_out,
               "collision": chosen_collision, "attempts": attempts}
    out = os.path.join(RESULTS_DC, "dc_R%d.json" % R)
    with open(out, "w") as f:
        json.dump(summary, f, indent=2, default=str)
    if chosen and chosen_out:
        dc = read_differential_characteristic(BLOCK, chosen_out, R)
        with open(os.path.join(RESULTS_DC, "dc_R%d.txt" % R), "w") as f:
            for row in dc:
                if isinstance(row, list):
                    for line in row:
                        f.write(str(line) + "\n")
                else:
                    f.write(str(row) + "\n")
    if validate and chosen and chosen_collision:
        import find_collision
        find_collision._save(R, chosen["candidate"], chosen_collision)
    return summary


def run_round(R, lc_json=None, timeout=300, max_attempts=8, init_pro=97,
              validate=True, oracle_timeout=300, max_validate=1, solve_budget=None,
              o5_value=True):
    specs = build_specs(R, lc_json)
    return run_specs(R, specs, timeout=timeout, max_attempts=max_attempts,
                     init_pro=init_pro, validate=validate, oracle_timeout=oracle_timeout,
                     max_validate=max_validate, solve_budget=solve_budget, o5_value=o5_value)


if __name__ == "__main__":
    # usage: python3 dc_search.py R [per_call_timeout_s] [max_attempts] [o5_value 0|1]
    R = int(sys.argv[1])
    timeout = int(sys.argv[2]) if len(sys.argv) > 2 else 300
    max_attempts = int(sys.argv[3]) if len(sys.argv) > 3 else 8
    o5_value = bool(int(sys.argv[4])) if len(sys.argv) > 4 else True
    run_round(R, timeout=timeout, max_attempts=max_attempts, o5_value=o5_value)
